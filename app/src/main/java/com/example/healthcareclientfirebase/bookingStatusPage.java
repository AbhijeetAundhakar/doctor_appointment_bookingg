public class bookingStatusPage extends AppCompatActivity {

    ImageView imageView;
    TextView textBooking;

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        EdgeToEdge.enable(this);
        setContentView(R.layout.activity_booking_status_page);
        ViewCompat.setOnApplyWindowInsetsListener(findViewById(R.id.main), (v, insets) -> {
            Insets systemBars = insets.getInsets(WindowInsetsCompat.Type.systemBars());
            v.setPadding(systemBars.left, systemBars.top, systemBars.right, systemBars.bottom);
            return insets;
        });

        imageView = (ImageView) findViewById(R.id.imageView);
        textBooking = (TextView) findViewById(R.id.textViewBookingSuccessfully);

        Animation trans = AnimationUtils.loadAnimation(getApplicationContext(), R.anim.translate);
        imageView.setAnimation(trans);
        textBooking.setAnimation(trans);

    }
}